package com.bhanu.webservice.beans;

public class TbtMapping {

private String appId;

    private String appSecret;

    private String assetId;

    private String systemUserId;

    private String systemUserName;

    private String systemUserPassword;

    private String offlineRealm;

    private String dateTimeHeader;

    /**
     * @return the appId
     */
    public String getAppId() {
        return appId;
    }

    /**
     * @param appId the appId to set
     */
    public void setAppId(String appId) {
        this.appId = appId;
    }

    /**
     * @return the appSecret
     */
    public String getAppSecret() {
        return appSecret;
    }

    /**
     * @param appSecret the appSecret to set
     */
    public void setAppSecret(String appSecret) {
        this.appSecret = appSecret;
    }

    /**
     * @return the assetId
     */
    public String getAssetId() {
        return assetId;
    }

    /**
     * @param assetId the assetId to set
     */
    public void setAssetId(String assetId) {
        this.assetId = assetId;
    }

    /**
     * @return the systemUserId
     */
    public String getSystemUserId() {
        return systemUserId;
    }

    /**
     * @param systemUserId the systemUserId to set
     */
    public void setSystemUserId(String systemUserId) {
        this.systemUserId = systemUserId;
    }

    /**
     * @return the systemUserName
     */
    public String getSystemUserName() {
        return systemUserName;
    }

    /**
     * @param systemUserName the systemUserName to set
     */
    public void setSystemUserName(String systemUserName) {
        this.systemUserName = systemUserName;
    }

    /**
     * @return the systemUserPassword
     */
    public String getSystemUserPassword() {
        return systemUserPassword;
    }

    /**
     * @param systemUserPassword the systemUserPassword to set
     */
    public void setSystemUserPassword(String systemUserPassword) {
        this.systemUserPassword = systemUserPassword;
    }

    /**
     * @return the offlineRealm
     */
    public String getOfflineRealm() {
        return offlineRealm;
    }

    /**
     * @param offlineRealm the offlineRealm to set
     */
    public void setOfflineRealm(String offlineRealm) {
        this.offlineRealm = offlineRealm;
    }

    /**
     * @return the dateTimeHeader
     */
    public String getDateTimeHeader() {
        return dateTimeHeader;
    }

    /**
     * @param dateTimeHeader the dateTimeHeader to set
     */
    public void setDateTimeHeader(String dateTimeHeader) {
        this.dateTimeHeader = dateTimeHeader;
    }
}
